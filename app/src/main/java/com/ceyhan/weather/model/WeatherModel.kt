package com.ceyhan.weather.model

data class Weather(
    val queryCost: Long,
    val latitude: Double,
    val longitude: Double,
    val resolvedAddress: String,
    val address: String,
    val timezone: String,
    val tzoffset: Double,
    val description: String,
    val days: List<Day>,
    val alerts: List<Any?>,
    val currentConditions: CurrentConditions,
)

