    val windspeed: Double,
    val winddir: Double,
    val pressure: Double,
    val cloudcover: Double,
    val visibility: Double,
    val solarradiation: Double,
    val solarenergy: Double,
    val uvindex: Double,
    val severerisk: Double,
    val sunrise: String,
    val sunriseEpoch: Long,
    val sunset: String,
